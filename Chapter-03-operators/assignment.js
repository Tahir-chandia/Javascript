// Assignment operators

let num = 2; // = is assignment operator

console.log(num);

num = num + 2; // it become num(2) + 2 = 4

console.log(num);

num += 2; // it become  += 4 + 2 = 6

console.log(num);

num -= 2; // 6 - 2 =4

console.log(num);

num *= 2; // 4*2 =8

console.log(num);

num /= 2; // 4/2 =4

console.log(num);
num %= 3; // % 4 // 3 =1

console.log(num);

num **= 3; // % 1 ** 3 =1

console.log(num);
