console.log("== ",5 == 5);// check value if value equal to other value it return true

console.log("== ",5 == "5");// check value if value equal to other value it return true

console.log("=== ",5 === "5");// check first data type and then value if data type and value equal to other 
// data type and value it return true

console.log("!= ",5 != 5);// check value if value  equal to other value it return false 

console.log("!= ","5" !== 5);// check first data type and then value if data type and value equal to other 
// data type and value it return false

console.log("> ",5 > 3 ); // if the left value is greater than right value it return true 

console.log("< ",5 < 3 ); // if the left value is less than right value it return true 

console.log(">= ",5 >= 3 ); // if the left value is greater than or equal to right value it return true 

console.log("<= ",5 <= 3 ); // if the left value is less than or equal to right value it return true 
