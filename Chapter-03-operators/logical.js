//Logical Operators (Combine conditions)

// Use to combine more than one condition

//1 && (And) Operator

//In this operator when both conditions are true it gives true otherwise it give false

console.log("true && true",true && true);
console.log("true && false",true && false);
console.log("false && false",false && false);
console.log("true && true",true && true,"\n");

//2 || (Or) Operator

//In this operator when any one conditions are true it gives true otherwise it give false


console.log("true || true",true || true);
console.log("true || false",true || false);
console.log("false || false",false || false);
console.log("true || true",true || true,"\n");

//3 ! (Not) Operator

//In this operator when  condition is true it gives false and when  condition is false it gives true

console.log("!true",!true);
console.log("!false",!false);