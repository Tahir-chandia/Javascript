//Increament/Decreament(Unary Operator) (Add or Subtract 1)


// Increment

console.log("\nIncrement\n");
let a = 5

a++ // post increament it will add 1 after use by a one time 
console.log(a); // 5

console.log(a); // 6

++a // pre increament it will add 1 before use by a one time

console.log(a); //7


// Decrement
console.log("\nDecrement\n");

let b = 5

b-- // post decreament it will subtract 1 after use by b one time 
console.log(b); // 5

console.log(b); // 4

--b // pre increament it will subtract 1 before use by b one time

console.log(3); //3


