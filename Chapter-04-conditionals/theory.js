/*A Conditional statement lets your code make decisions. It checks if something is true or 
false, and then runs certain code based on that.*/

let condition =undefined
if (condition){
    // code run if condition is true
}
else{
    // code run if condition is false
}

// For Example:" If it's raining, take an umbrella. Otherwise, enjoy the sunshine!"

let weather = "rainy"

if(weather=="rainy"){
    console.log("Take an umbrella");
}else{
    console.log("Enjoy the sunshine!");
    
}


